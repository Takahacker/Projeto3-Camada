def cria_datagrama(num_pacote, total_pacotes, payload):
    head = struct.pack('>III', num_pacote, total_pacotes, len(payload))
    eop = b'\xAA\xBB\xCC'  # Exemplo de EOP
    datagrama = head + payload + eop
    return datagrama

